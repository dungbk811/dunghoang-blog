// Helper functions
export function getStats(type: 'learning' | 'coo', customItems?: RoadmapItem[]): { total: number; completed: number; inProgress: number; planned: number } {
  const items = customItems || (type === 'learning' ? learningRoadmap : cooRoadmap);
  return {
    total: items.length,
    completed: items.filter((item) => item.status === 'completed').length,
    inProgress: items.filter((item) => item.status === 'in-progress').length,
    planned: items.filter((item) => item.status === 'planned').length,
  };
}

export function getItemsByCategory(type: 'learning' | 'coo'): Record<string, RoadmapItem[]> {
  const items = type === 'learning' ? learningRoadmap : cooRoadmap;
  return items.reduce(
    (acc, item) => {
      if (!acc[item.category]) {
        acc[item.category] = [];
      }
      acc[item.category].push(item);
      return acc;
    },
    {} as Record<string, RoadmapItem[]>
  );
}

export function getRecentItems(type: 'learning' | 'coo', limit: number = 5): RoadmapItem[] {
  const items = type === 'learning' ? learningRoadmap : cooRoadmap;
  return items
    .filter((item) => item.status === 'in-progress' || item.status === 'completed')
    .sort((a, b) => {
      const dateA = a.progress?.lastUpdated || a.startDate || '';
      const dateB = b.progress?.lastUpdated || b.startDate || '';
      return dateB.localeCompare(dateA);
    })
    .slice(0, limit);
}
